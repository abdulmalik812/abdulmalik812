def rebuild_svg(filename, url):
    # 1. Download original SVG
    urllib.request.urlretrieve(url, filename)
    
    ET.register_namespace('', 'http://www.w3.org/2000/svg')
    tree = ET.parse(filename)
    root = tree.getroot()
    
    # 2. Update width to 1200px, height to 650px
    if 'width' in root.attrib:
        root.attrib['width'] = "1200px"
    if 'height' in root.attrib:
        root.attrib['height'] = "650px"
        
    for rect in root.findall('.//{http://www.w3.org/2000/svg}rect'):
        rect.attrib['width'] = "1200px"
        rect.attrib['height'] = "650px"
        
    # 3. Inject ASCII art at x=15 and font-size 9px
    for text_elem in root.findall('.//{http://www.w3.org/2000/svg}text'):
        if text_elem.get('class') == 'ascii':
            # Clear old tspans
            for child in list(text_elem):
                text_elem.remove(child)
                
            text_elem.set('font-size', '9px')
            
            lines = ascii_art.split('\n')
            y_offset = 30
            for line in lines:
                tspan = ET.SubElement(text_elem, '{http://www.w3.org/2000/svg}tspan')
                tspan.set('x', '15')
                tspan.set('y', str(y_offset))
                tspan.text = line
                y_offset += 11
            
            continue
            
        # 4. Shift stats text (and all its tspans) from x=390 to x=580
        if text_elem.get('x') == '390':
            text_elem.set('x', '580')
            for tspan in text_elem.findall('.//{http://www.w3.org/2000/svg}tspan'):
                if tspan.get('x') == '390':
                    tspan.set('x', '580')

    tree.write(filename, encoding="utf-8", xml_declaration=True)

